public class SwitchExample4 {
    public static void main(String[] args) {
        char ch='d';
        switch(ch)
        {
            case 't':System.out.println("tarun");break;
            case 'a':System.out.println("aman");break;
            case 'p':System.out.println("pawan");break;
            case 'd':System.out.println("deepak");break;
        }
    }
   
}
